function getInitialLanguage(): HomeLanguage {
  const stored = localStorage.getItem(HOME_LANGUAGE_KEY);
  if (stored === "uz" || stored === "ru" || stored === "en") return stored;
  const browserLanguage = navigator.language.toLowerCase();
  if (browserLanguage.startsWith("ru")) return "ru";
  if (browserLanguage.startsWith("uz")) return "uz";
  return "en";
}

function translateHomeText(text: string, language: HomeLanguage, values?: TranslationValues) {
  const translated = language === "en"
    ? text
    : supplementalDictionaries[language][text] ?? dictionaries[language][text] ?? text;

  return translated.replace(/\{(\w+)\}/g, (token, key: string) => (
    values?.[key] === undefined ? token : String(values[key])
  ));
}

const HomeI18nContext = createContext<HomeI18nContextValue | null>(null);

export function HomeI18nProvider({ children }: { children: ReactNode }) {
  const [language, setLanguageState] = useState<HomeLanguage>(() => getInitialLanguage());

  useEffect(() => {
    document.documentElement.lang = language;
    document.title = translateHomeText(HOME_PAGE_TITLE, language);
    localStorage.setItem(HOME_LANGUAGE_KEY, language);
  }, [language]);

  const value = useMemo<HomeI18nContextValue>(
    () => ({
      language,
      setLanguage: setLanguageState,
      t: (text, values) => translateHomeText(text, language, values),
    }),
    [language],
  );

  return <HomeI18nContext.Provider value={value}>{children}</HomeI18nContext.Provider>;
}

export function useHomeI18n() {
  const context = useContext(HomeI18nContext);
  if (!context) {
    throw new Error("useHomeI18n must be used inside HomeI18nProvider");
  }
  return context;
}
