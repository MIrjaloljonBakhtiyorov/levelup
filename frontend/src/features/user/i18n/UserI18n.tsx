const textNodeOriginals = new WeakMap<Text, string>();
const attributeOriginals = new WeakMap<Element, Map<string, string>>();

const translatedAttrs = ["aria-label", "placeholder", "title"];

function normalize(text: string) {
  return text.replace(/\s+/g, " ").trim();
}

function applyCountRules(text: string, language: Exclude<UserLanguage, "uz">) {
  const rules: [RegExp, (match: RegExpMatchArray) => string][] = [
    [/^(\d+) ta kurs$/, (match) => language === "en" ? `${match[1]} courses` : `${match[1]} курсов`],
    [/^(\d+) ta resurs$/, (match) => language === "en" ? `${match[1]} resources` : `${match[1]} ресурсов`],
    [/^(\d+) ta audio material$/, (match) => language === "en" ? `${match[1]} audio materials` : `${match[1]} аудио материалов`],
    [/^(\d+) ta xabar$/, (match) => language === "en" ? `${match[1]} messages` : `${match[1]} сообщений`],
    [/^(\d+) ta yangi xabar sizni kutyapti$/, (match) => language === "en" ? `${match[1]} new messages are waiting for you` : `Вас ждут ${match[1]} новых сообщений`],
    [/^(\d+) kun$/, (match) => language === "en" ? `${match[1]} days` : `${match[1]} дней`],
    [/^(\d+) soat oldin$/, (match) => language === "en" ? `${match[1]} hours ago` : `${match[1]} ч. назад`],
    [/^(\d+) daqiqa oldin$/, (match) => language === "en" ? `${match[1]} minutes ago` : `${match[1]} мин. назад`],
    [/^(\d+) kun oldin$/, (match) => language === "en" ? `${match[1]} days ago` : `${match[1]} дн. назад`],
  ];

  for (const [pattern, translate] of rules) {
    const match = text.match(pattern);
    if (match) return translate(match);
  }

  return text;
}

export function translateUserText(text: string, language: UserLanguage) {
  if (language === "uz") return text;

  const clean = normalize(text);
  const translated = dictionaries[language][clean] ?? applyCountRules(clean, language);
  return text.replace(clean, translated);
}

function rememberAttribute(element: Element, attr: string, value: string) {
  const existing = attributeOriginals.get(element) ?? new Map<string, string>();
  if (!existing.has(attr)) {
    existing.set(attr, value);
    attributeOriginals.set(element, existing);
  }
  return existing.get(attr) ?? value;
}

function translateElement(element: Element, language: UserLanguage) {
  translatedAttrs.forEach((attr) => {
    const value = element.getAttribute(attr);
    if (!value) return;

    const original = rememberAttribute(element, attr, value);
    const translated = translateUserText(original, language);
    if (element.getAttribute(attr) !== translated) {
      element.setAttribute(attr, translated);
    }
  });

  element.childNodes.forEach((node) => {
    if (node.nodeType === Node.TEXT_NODE) {
      const textNode = node as Text;
      const original = textNodeOriginals.get(textNode) ?? textNode.nodeValue ?? "";

      if (!textNodeOriginals.has(textNode)) {
        textNodeOriginals.set(textNode, original);
      }

      if (normalize(original)) {
        const translated = translateUserText(original, language);
        if (textNode.nodeValue !== translated) {
          textNode.nodeValue = translated;
        }
      }
      return;
    }

    if (node.nodeType === Node.ELEMENT_NODE) {
      translateElement(node as Element, language);
    }
  });
}

function readInitialLanguage(): UserLanguage {
  const stored = localStorage.getItem(USER_LANGUAGE_KEY);
  return stored === "ru" || stored === "en" || stored === "uz" ? stored : "uz";
}

const UserI18nContext = createContext<UserI18nContextValue | null>(null);

export function UserI18nProvider({ children }: { children: ReactNode }) {
  const [language, setLanguageState] = useState<UserLanguage>(readInitialLanguage);

  const value = useMemo<UserI18nContextValue>(() => ({
    language,
    setLanguage(nextLanguage) {
      localStorage.setItem(USER_LANGUAGE_KEY, nextLanguage);
      document.documentElement.lang = nextLanguage;
      setLanguageState(nextLanguage);
    },
    t(text) {
      return translateUserText(text, language);
    },
  }), [language]);

  useEffect(() => {
    document.documentElement.lang = language;
    const root = document.querySelector(".user-layout");
    if (!root) return;

    const update = () => translateElement(root, language);
    update();

    const observer = new MutationObserver(update);
    observer.observe(root, {
      attributes: true,
      attributeFilter: translatedAttrs,
      childList: true,
      subtree: true,
    });

    return () => observer.disconnect();
  }, [language]);

  return (
    <UserI18nContext.Provider value={value}>
      {children}
    </UserI18nContext.Provider>
  );
}

export function useUserI18n() {
  const context = useContext(UserI18nContext);

  if (!context) {
    throw new Error("useUserI18n must be used inside UserI18nProvider");
  }

  return context;
}
